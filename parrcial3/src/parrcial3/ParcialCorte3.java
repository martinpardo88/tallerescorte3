    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        try {
       
        String rutaArchivo = "C:\\Users\\PC\\Documents\\NetBeansProjects\\parrcial3\\contactos.xml";
        File archivo = new File(rutaArchivo);

        

        
        DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
        DocumentBuilder builder = factory.newDocumentBuilder();
        org.w3c.dom.Document doc = builder.parse(archivo);
        doc.getDocumentElement().normalize();

        
         org.w3c.dom.NodeList listaContactos = doc.getElementsByTagName("contacto");

      
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) jTable1.getModel();

        
        modelo.setRowCount(0);

        
        for (int i = 0; i < listaContactos.getLength(); i++) {
                 org.w3c.dom.Node nodo = listaContactos.item(i);
        if (nodo.getNodeType() == org.w3c.dom.Node.ELEMENT_NODE) {
            org.w3c.dom.Element elemento = (org.w3c.dom.Element) nodo;

                
                String cedula = elemento.getElementsByTagName("cedula").item(0).getTextContent();
                String nombre = elemento.getElementsByTagName("nombre").item(0).getTextContent();
                String correo = elemento.getElementsByTagName("correo").item(0).getTextContent();

               
                modelo.addRow(new Object[]{cedula, nombre, correo});
            }
        }

      

    } catch (Exception e) {
            System.out.println("no se encotro el XML");
    }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void agregarJsonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarJsonActionPerformed
        
    String cedula = campocedula.getText();
    String nombre = camponombre.getText();
    String correo = campocorreo.getText();

  

    try {
       
        com.google.gson.JsonObject contacto = new com.google.gson.JsonObject();
        contacto.addProperty("cedula", cedula);
        contacto.addProperty("nombre", nombre);
        contacto.addProperty("correo", correo);

       
        String rutaArchivo = "C:\\Users\\PC\\Documents\\NetBeansProjects\\parrcial3\\contactos.json";
        com.google.gson.JsonArray jsonArray = leerArchivoJson(rutaArchivo);

       
        
        jsonArray.add(contacto);

        
        com.google.gson.Gson gson = new com.google.gson.Gson();
        String jsonString = gson.toJson(jsonArray);

       
        try (java.io.FileWriter writer = new java.io.FileWriter(rutaArchivo)) {
            writer.write(jsonString);
            System.out.println("se guardon el archivo en la ruta");
        }

       
        campocedula.setText("");
        camponombre.setText("");
        campocorreo.setText("");

    } catch (java.io.IOException e) {
        System.out.println("no se escribio nada en el archivo");
    }
}


    private com.google.gson.JsonArray leerArchivoJson(String ruta) {
        com.google.gson.JsonArray jsonArray = new com.google.gson.JsonArray();
        java.io.File archivo = new java.io.File(ruta);

        if (archivo.exists()) {
            try (java.io.FileReader reader = new java.io.FileReader(archivo)) {
                com.google.gson.Gson gson = new com.google.gson.Gson();

                jsonArray = gson.fromJson(reader, com.google.gson.JsonArray.class);
                if (jsonArray == null) {
                    jsonArray = new com.google.gson.JsonArray(); 
                }
            } catch (java.io.IOException e) {
                System.out.println("no se pudo leer el archivo json");
            }
    }

    return jsonArray;
        
        
    }//GEN-LAST:event_agregarJsonActionPerformed

