        correoElem.appendChild(doc.createTextNode(correo));
        contacto.appendChild(correoElem);

       
        TransformerFactory transformerFactory = TransformerFactory.newInstance();
        Transformer transformer = transformerFactory.newTransformer();
        transformer.setOutputProperty(OutputKeys.INDENT, "yes");

        
        String rutaArchivo = "C:\\Users\\PC\\Documents\\NetBeansProjects\\parrcial3\\contactos.xml";
        File archivoSalida = new File(rutaArchivo);

        StreamResult result = new StreamResult(archivoSalida);
        DOMSource source = new DOMSource(doc);
        transformer.transform(source, result);

        

        
